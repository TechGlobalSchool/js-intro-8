let name = 'Jane'; // name is the variable and 'Jane' is the string data

console.log(name); // Jane

let age = 25; // age is the variable and 25 is the number data

console.log(age); // 25


// Create a variable named as city and store Chicago inside the variable and then output it.

// variable initialization with the value
let city = 'Chicago';
