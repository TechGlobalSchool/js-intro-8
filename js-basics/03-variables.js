// variable initialization with the value vs variable declaration vs variable assignment
let country; // variable declaration - reserve some memory to store information

console.log(country); // undefined

country = 'US'; // assignment - here we are assigning a value to previously declared variable

console.log(country); // US

country = 'Canada'; // re-assignment

console.log(country); // Canada



